package Island.domain;

public class Coordinates {
    private int y;
    private int x;

    public Coordinates(int y, int x) {
        this.x = x;
        this.y = y;
    }

    public void setX(int x) {
        this.x = x;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}
